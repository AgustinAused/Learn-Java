package operaciones;

public class Operaciones {
    
    public static double sumar(double a, double n){
        System.out.println("sumar(double a, double n)");
        return a + n;
    }
    
    public static int sumar(int a, int b){
        System.out.println("sumar(int a, int b");
        return a + b;
    }
    
}
